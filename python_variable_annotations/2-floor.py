#!/usr/bin/env python3
"""
Ce module contient une fonction qui retourne le floor d'un nombre flottant.
"""

import math


def floor(n: float) -> int:
    """
    Retourne le floor d'un nombre flottant.

    Args:
        n (float): Le nombre flottant.

    Returns:
        int: Le floor du nombre donn√©.
    """
    return math.floor(n)
