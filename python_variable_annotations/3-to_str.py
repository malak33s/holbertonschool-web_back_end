#!/usr/bin/env python3
"""
Ce module contient une fonction qui retourne la représentation sous forme
de chaîne de caractères d'un nombre flottant.
"""


def to_str(n: float) -> str:
    """
    Retourne la représentation en chaîne de caractères d'un nombre flottant.

    Args:
        n (float): Le nombre flottant.

    Returns:
        str: La représentation en chaîne du nombre flottant.
    """
    return str(n)
